function add(){
    document.getElementById("addoutput").innerHTML = parseInt(document.getElementById("input1").value) + parseInt(document.getElementById("input2").value);
}
function sub(){
    document.getElementById("addoutput").innerHTML = parseInt(document.getElementById("input1").value) - parseInt(document.getElementById("input2").value);
}
function mul(){
    document.getElementById("addoutput").innerHTML = parseInt(document.getElementById("input1").value) * parseInt(document.getElementById("input2").value);
}
function div(){
    document.getElementById("addoutput").innerHTML = parseInt(document.getElementById("input1").value) / parseInt(document.getElementById("input2").value);
}